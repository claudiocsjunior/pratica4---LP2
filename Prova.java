class Prova{
	private double notaParte1;
	private double notaParte2;
	
	public Prova(double nota1_recebida, double nota2_recebida){
		this.notaParte1 = nota1_recebida;
		this.notaParte2 = nota2_recebida;	
	}

	public double calcularNotaTotal(){
		double total = (this.notaParte1 + this.notaParte2);
		return total > 10 ? 10 : total; 	
	}
}
